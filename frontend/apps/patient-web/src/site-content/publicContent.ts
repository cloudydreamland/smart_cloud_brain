function makeStaticPage(item: (typeof defaultStaticPages)[number]): PublicPage {
  const details = pageSpecificDetails[item.routeName] || [];
  return {
    label: item.label,
    title: item.title,
    intro: item.intro,
    emergency: item.routeName === "service-emergency" ? "出现疑似急症时，请直接前往急诊或拨打急救电话，不要等待线上分诊、搜索或普通预约。" : undefined,
    blocks: item.points.map((point, index) => ({
      title: point.title,
      summary: point.text,
      details: details[index] || defaultDetails(point.title, item.routeName),
      links: index === 0 && item.primary ? [{ label: item.primary.label, to: { name: item.primary.routeName, query: item.primary.query } }] : undefined,
    })),
    related: relatedFor(item.routeName),
  };
}

